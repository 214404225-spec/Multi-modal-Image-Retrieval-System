"""
意图识别模块的Prompt模板定义
"""

from langchain_core.prompts import PromptTemplate

# 意图识别的Prompt模板
INTENT_RECOGNITION_PROMPT = PromptTemplate(
    input_variables=["query"],
    template="""你是一个智能意图识别助手。分析用户查询，判断是否需要图像检索并提取关键信息。

用户查询："{query}"

请严格按以下格式输出（每行一个字段，冒号分隔）：

是否需要检索：[是/否]
检索类别：[查询要搜索的主要物体，例如：狗、猫、熊、汽车、键盘、椅子。不要填"无"除非真的不需要检索]
检索数量：[具体数字如"2"、"5"，或模糊描述如"很多"、"一些"。没有则填无]
检索方式：[具体数字→TopK，模糊词→卡阈值]
属性条件：[物体的修饰特征，多个用逗号分隔。常见类别：
- 颜色：棕色、黑色、白色、黄色、灰色、红色、蓝色、绿色
- 姿态：站立、坐着、躺着、奔跑、跳跃、飞翔
- 场景：室内、室外、草地上、水中、天空
- 构图：全身、半身、局部、特写
- 视角：正面、侧面、背面、俯视、仰视
- 背景：纯色背景、白色背景、自然背景
- 光照：明亮、昏暗、逆光
- 大小：大、小
没有属性则填无]

示例：
查询："帮我找两只棕色的狗"
输出：
是否需要检索：是
检索类别：狗
检索数量：2
检索方式：TopK
属性条件：棕色
"""
)
